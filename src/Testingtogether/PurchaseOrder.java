    private void btnNewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNewActionPerformed
        // TODO add your handling code here:
        
         try
        {
         
               Connection con=DriverManager.getConnection(url,user,pass);
               String sql="select * from PurchaseOrder";
               PreparedStatement pst=con.prepareStatement(sql);
               ResultSet rs=pst.executeQuery();
               DefaultTableModel POTM=(DefaultTableModel)POtable.getModel();
               POTM.setRowCount(0);
               
               while(rs.next())
               {
                   Object o[]={rs.getString("PurchaseOrderID"),rs.getString("PurchaseOrderDate"),rs.getString("Receipt"),rs.getString("SubContractorID"),rs.getString("WorkOrderID")};
                   POTM.addRow(o);
               }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this, e);
        }
        
        
    }//GEN-LAST:event_btnNewActionPerformed

